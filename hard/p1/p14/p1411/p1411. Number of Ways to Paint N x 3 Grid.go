package p1411

const MOD = 1e9 + 7

/*

Gen1
010 101 201
012 102 202
020 120 210
021 121 212

6w
6o



===============
Gen2

010 -> 101 201 102 202 121		3w 2o
012 -> 101 201 120 121			2w 2o
020 -> 101 201 102 202 212		3w 2o
021 -> 102 202 210 212			2w 2o

101 -> 010 012 020 210 212		3w 2o
102 -> 010 020 210 021			2w 2o
120 -> 012 201 202 212			2w 2o
121 -> 010 012 202 210 212		3w 2o



...


6*(3w 2o) = 18w +12o
6*(2w 2o) = 12w +12o

30w
24o
===============

=>
w=3w 2o
o=2w 2o


*/

func numOfWays(n int) int {
	w := 6
	o := 6
	for i := 1; i < n; i++ {
		w, o = (3*w+2*o)%MOD, (2*w+2*o)%MOD
	}
	res := (w + o) % MOD
	return res
}
