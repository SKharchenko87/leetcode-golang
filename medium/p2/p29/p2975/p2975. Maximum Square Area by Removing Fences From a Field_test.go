func Test_maximizeSquareArea(t *testing.T) {
	type args struct {
		m       int
		n       int
		hFences []int
		vFences []int
	}
	tests := []struct {
		name string
		args args
		want int
	}{
		{"Example 1", args{m: 4, n: 3, hFences: []int{2, 3}, vFences: []int{2}}, 4},
		{"Example 2", args{m: 6, n: 7, hFences: []int{2}, vFences: []int{4}}, -1},
		{"TestCase 8", args{m: 4, n: 5, hFences: []int{2}, vFences: []int{4}}, 9},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := maximizeSquareArea(tt.args.m, tt.args.n, tt.args.hFences, tt.args.vFences); got != tt.want {
				t.Errorf("maximizeSquareArea() = %v, want %v", got, tt.want)
			}
		})
	}
}

