package p0841

import "testing"

func Test_canVisitAllRooms(t *testing.T) {
	type args struct {
		rooms [][]int
	}
	tests := []struct {
		name string
		args args
		want bool
	}{
		{"Case 1", args{[][]int{{1}, {2}, {3}, {}}}, true},
		{"Case 2", args{[][]int{{1, 3}, {3, 0, 1}, {2}, {0}}}, false},
		{"Case 61", args{[][]int{{7, 16, 8, 19}, {10}, {9, 11}, {3, 14, 16, 19}, {8, 10, 19, 1, 7}, {13, 5, 10, 15, 4}, {6, 13}, {14, 11, 12, 18}, {3}, {17, 9}, {1, 2, 6, 9}, {12, 2}, {4}, {2, 13, 17}, {17}, {}, {11, 15}, {3, 5}, {15, 18, 5}, {7, 18, 1}}}, true},
		{"Case 62", args{[][]int{{10, 35, 25, 33}, {}, {47, 27}, {23, 3, 28, 39}, {36, 41, 10, 24}, {14, 40, 42, 44}, {41, 35}, {48}, {24, 26, 44, 23, 11, 17}, {1, 17, 20, 30}, {}, {38, 39, 44}, {}, {11, 21, 45, 13}, {1, 27, 14, 19}, {23}, {30, 21, 8, 22, 40}, {14, 48}, {32, 6}, {5, 15, 26, 34, 38}, {43, 25}, {18, 10, 33}, {15, 34, 9}, {18, 46, 48, 7, 13}, {5, 29, 4}, {42, 34}, {7, 13, 37, 8, 15, 21}, {4, 5}, {38, 20, 42}, {16, 19, 47}, {8, 29}, {28, 33, 37, 49}, {9, 39, 49, 41}, {31, 12, 26, 32}, {2, 40, 32, 46}, {27, 22}, {12, 37, 2}, {31, 1}, {46, 19, 16, 18, 30}, {35}, {6, 3, 7, 28, 43}, {4, 25, 2, 29}, {}, {22, 24, 45, 12}, {36, 31}, {3, 36, 45}, {20, 43, 49}, {11}, {16, 6}, {17, 47}}}, true},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := canVisitAllRooms(tt.args.rooms); got != tt.want {
				t.Errorf("canVisitAllRooms() = %v, want %v", got, tt.want)
			}
		})
	}
}
