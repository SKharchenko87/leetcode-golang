package p0452

import "testing"

func Test_findMinArrowShots(t *testing.T) {
	type args struct {
		points [][]int
	}
	tests := []struct {
		name string
		args args
		want int
	}{
		{"Case 1", args{[][]int{{10, 16}, {2, 8}, {1, 6}, {7, 12}}}, 2},
		{"Case 2", args{[][]int{{1, 2}, {3, 4}, {5, 6}, {7, 8}}}, 4},
		{"Case 3", args{[][]int{{1, 2}, {2, 3}, {3, 4}, {4, 5}}}, 2},
		{"Case 32/50", args{[][]int{{4289383, 51220269}, {81692777, 96329692}, {57747793, 81986128}, {19885386, 69645878}, {96516649, 186158070}, {25202362, 75692389}, {83368690, 85888749}, {44897763, 112411689}, {65180540, 105563966}, {4089172, 7544908}}}, 4},
		{"Case 4", args{[][]int{{3, 9}, {7, 12}, {3, 8}, {6, 8}, {9, 10}, {2, 9}, {0, 9}, {3, 9}, {0, 6}, {2, 8}}}, 2},
		{"Case 7", args{[][]int{{4289383, 51220269}, {81692777, 96329692}, {57747793, 81986128}, {19885386, 69645878}, {96516649, 186158070}, {25202362, 75692389}, {83368690, 85888749}, {44897763, 112411689}, {65180540, 105563966}, {4089172, 7544908}}}, 4},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := findMinArrowShots(tt.args.points); got != tt.want {
				t.Errorf("findMinArrowShots() = %v, want %v", got, tt.want)
			}
		})
	}
}
