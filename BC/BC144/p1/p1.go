package p1

func canAliceWin(n int) bool {
	if n < 10 {
		return false
	}
	winAlice := true
	i := 10
	for n >= 0 {
		winAlice = !winAlice
		n -= i
		i--
	}
	return winAlice
}

func abs(x int) int {
	if x >= 0 {
		return x
	}
	return -1 * x
}

